import Foundation

/**
 Card ranks (Ace, 2, 3, 4, 5, 6, 7, 8, 9, 10, Jack, Queen, King)
 */
enum CardRank: Int {
    case ACE    = 0
    case TWO    = 1
    case THREE  = 2
    case FOUR   = 3
    case FIVE   = 4
    case SIX    = 5
    case SEVEN  = 6
    case EIGHT  = 7
    case NINE   = 8
    case TEN    = 9
    case JACK   = 10
    case QUEEN  = 11
    case KING   = 12
}
